function addNumbers(a, b){
    return a + b;
}
function multiplyNumbers(a, b){
    return a * b;
}
function subtractNumbers(a, b){
    return a - b;
}
function divideNumbers(a, b){
    return a / b;
}
console.log("Addition - ", addNumbers(3, 5));
console.log("Multiplication - ", multiplyNumbers(3, 5));
console.log("Subtraction - ", subtractNumbers(3, 5));
console.log("Divison - ", divideNumbers(3, 5));